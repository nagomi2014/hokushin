// ------------------------------------------------------------
// Coach context types
// ------------------------------------------------------------

export type CoachContext =
  | { kind: "pyramid"; level: PyramidLevel }
  | { kind: "field"; fieldId: FieldId }
  | { kind: "monthly" };

export function getCoachPrompt(ctx: CoachContext): CoachPrompt {
  if (ctx.kind === "pyramid") return PYRAMID_COACH_PROMPTS[ctx.level];
  if (ctx.kind === "field") return FIELD_COACH_PROMPTS[ctx.fieldId];
  return MONTHLY_COACH_PROMPT;
}

export function getCoachLabel(ctx: CoachContext): string {
  if (ctx.kind === "pyramid") {
    const names = ["", "人生理念", "人生のビジョン", "目標の設定", "計画", "日々の実践"];
    return `成功のピラミッド · ${names[ctx.level]}`;
  }
  if (ctx.kind === "field") {
    const names = [
      "", "健康・体力", "人間関係", "家族・家庭", "仕事・職業",
      "能力開発", "経済・蓄財", "趣味・教養",
    ];
    return `七つの分野 · ${names[ctx.fieldId]}`;
  }
  return "今月のアクションプラン";
}
